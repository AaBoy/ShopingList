package edu.pernat.shopinglist.android.razredi;

public class Uporabniki {

	String uporabnisko;
	String geslo;
	
	public Uporabniki(String u, String g)
	{
		uporabnisko=u;
		geslo=g;
	}
	
	
	public String getUporabnisko()
	{
		return uporabnisko;
		
	}
	
	public String getGeslo()
	{
		return geslo;
	}
}
